import React,{useEffect} from 'react'
import { TouchableOpacity, Image, StyleSheet,BackHandler,Alert } from 'react-native'
import { getStatusBarHeight } from 'react-native-status-bar-height'


export default function BackButton({navigation,goBack}) {

  useEffect(() => {
    const backAction = () => {
      Alert.alert("Are you sure you want to go back?", [
        {
          text: "Cancel",
          onPress: () => null,
          style: "cancel"
        },
        { text: "YES", onPress: () => navigation.navigate('DashboardForm') }
      ]);
      return true;
    };

    const backHandler = BackHandler.addEventListener(
      "hardwareBackPress",
      backAction
    );

    return () => backHandler.remove();
  }, []);
  return (
   
    <TouchableOpacity onPress={goBack} style={styles.container}>
      <Image
        style={styles.image}
        source={require('../assets/arrow_back.png')}
      />
    </TouchableOpacity>
  )
}

const styles = StyleSheet.create({
  container: {
    position: 'absolute',
    top: 10 + getStatusBarHeight(),
    left: 4,
  },
  image: {
    width: 24,
    height: 24,
  },
})
